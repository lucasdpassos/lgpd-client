1. Objetivo
A Política Externa de Privacidade da INOVA (“Política”) foi criada para demonstrar o seu compromisso com a privacidade e segurança de informações coletadas dos Usuários de serviços interativos oferecidos no site e nos aplicativos INOVA.
2. Quais dados pessoais serão tratados pela INOVA?
A INOVA poderá coletar as informações inseridas ativamente por você, “Usuário”, no momento do contato ou cadastro, através do preenchimento do formulário de contato ou pelo corretor online e, ainda, informações coletadas automaticamente ao utilizar os nossos serviços. Há, assim, o tratamento de dois tipos de dados pessoais: (i) os fornecidos pelo próprio Usuário e (ii) aqueles coletados automaticamente pela INOVA.
 
2.1. Dados Pessoais Fornecidos
pelo Usuário
A INOVA coleta todos os dados pessoais inseridos ou encaminhados ativamente pelo Usuário ao contatar ou se cadastrar em nosso portal. São eles: Nome completo, e-mail, telefone, data de nascimento, renda, CPF (titular e cônjuge), RG, Passaporte (se estrangeiro), estado civil, FGTS, vínculo empregatício, profissão e endereço completo. Independentemente de quais dados pessoais o Usuário fornece ativamente à INOVA, nós apenas faremos uso daqueles efetivamente relevantes e necessários para o alcance das finalidades a ele declaradas na coleta dos dados.
2.2. Dados coletados
automaticamente pela INOVA
A INOVA coleta uma série de informações de modo automático nos portais de interação com o Usuário, tais como: características do dispositivo de acesso, do navegador, IP (com data e hora), origem do IP, informações sobre cliques, páginas acessadas, termos de procura digitado em nossos portais, dentre outros. Para tal coleta, a INOVA fará uso de algumas tecnologias padrões, como cookies, pixel tags, beacons e local shared objects, que são utilizadas com o propósito de melhorar a sua experiência de navegação em nossos serviços, de acordo com os seus hábitos e preferências.
A INOVA não trata dados definidos como sensíveis, entendido como aqueles relacionados a origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico, tampouco dados de crianças, assim considerados os indivíduos menores de 12 anos, para as finalidades gerais aqui dispostas. Na remota hipótese em que for necessário o tratamento desses dados, solicitaremos o consentimento específico e destacado do Usuário ou dos responsáveis deste, com base nas hipóteses autorizadoras pela Lei Geral de Proteção de Dados.
 
3. Com quais finalidades a INOVA tratará os seus dados?
Os dados pessoais tratados pela INOVA têm como principais finalidades a possibilidade do Usuário conhecer a oferta de produtos e serviços pela plataforma, o fornecimento de informações via formulário de contato ou pelo corretor online, identificar os Usuários que navegam no site e manter os dados de Usuários atualizados.
A INOVA poderá utilizar as suas informações em demais serviços prestados diretamente pela INOVA, respeitadas as finalidades expostas e o consentimento que você previamente nos concedeu, quando houver a exigência legal dessa coleta.
Em alguns casos, poderá tratar seus dados pessoais quando necessários para o cumprimento de obrigação legal ou exercício regular de direitos em processo judicial, administrativo ou arbitral.
Além disso, também poderá tratar dados pessoais com base em seu interesse legítimo, sempre no limite de sua expectativa, e nunca em prejuízo de seus interesses, direitos e liberdades fundamentais.
Adicionalmente, as informações coletadas poderão, mediante a coleta prévia de sua autorização, serem utilizadas para fins publicitários, como para o envio de e-mail marketing ao usuário ou contatos por telefone, SMS, carta e aplicativos de mensagens como o WhatsApp. Caso você se sinta incomodado e não deseje mais receber quaisquer informativos publicitários da INOVA, você poderá, a qualquer momento, se descadastrar utilizando o botão “descadastre-se” presente nos e-mails ou nos contatar por meio do nosso SAC (21-3189-1100) manifestando a sua oposição.


4. Com quem a INOVA compartilhará os seus dados pessoais?
 
A INOVA utiliza-se, para a operacionalização de seus serviços, da parceria com diversas empresas localizadas Brasil e no exterior. Deste modo, a INOVA poderá compartilhar as suas informações pessoais, nas hipóteses abaixo expostas:
A. Com empresas parceiras e fornecedores no desenvolvimento e prestação de serviços voltados a você;
B. Com autoridades, entidades governamentais ou outros terceiros institucionais, para a proteção dos interesses da INOVA em qualquer tipo de conflito, incluindo ações judiciais e processos administrativos;
C. No caso de transações e alterações societárias envolvendo a INOVA, em que a transferência das informações será necessária para a continuidade dos serviços;
D. Mediante ordem judicial ou pelo requerimento de autoridades administrativas que detenham competência legal para a sua requisição.
Adicionalmente, é possível que algumas das transferências acima ocorram fora do território brasileiro. Ocasião em que a INOVA se compromete a fazê-lo somente para países que proporcionem um grau de proteção aos seus dados pessoais ao previsto na legislação aplicável, ou mediante a adoção de garantias e salvaguardas como cláusulas específicas, bem como mediante a prévia coleta do seu consentimento específico.
E. Com instituições financeiras, para análise e proteção de crédito.
 
 

5. Como a INOVA mantêm os seus dados pessoais seguros?
Qualquer informação fornecida pelo Usuário será coletada e guardada de acordo com os mais rígidos padrões de segurança. Para tanto, a INOVA adota diversas precauções, em observância às diretrizes sobre padrões de segurança estabelecidas nas legislações e sua Política de Segurança da Informação, tais como:
A. Utiliza os mais recentes métodos e equipamentos disponíveis no mercado para criptografar e anonimizar os seus dados pessoais, quando necessário;
B. Possui proteção contra acesso não autorizado a seus sistemas;
C. Somente autoriza o acesso de pessoas específicas ao local onde são armazenadas as suas informações pessoais, desde que este acesso seja essencial ao desenvolvimento da atividade pretendida;
D. Garante que aqueles agentes, funcionários internos ou parceiros externos que realizarem o tratamento de dados pessoais deverão se comprometer a manter o sigilo absoluto das informações acessadas, bem como de adotar as melhores práticas para manuseio destas informações, conforme determinado nas políticas e procedimentos.
O acesso às informações coletadas é restrito aos colaboradores e às pessoas autorizadas. Aqueles que se utilizarem indevidamente dessas informações, em violação desta Política de Privacidade e a de Segurança da Informação, estarão sujeitos a sanções disciplinares e legais cabíveis.
Além dos esforços técnicos, a INOVA também adota medidas institucionais visando a proteção de dados pessoais, de modo que mantém programa de privacidade aplicado às suas atividades, constantemente atualizado.
Embora a INOVA adote os melhores esforços no sentido de preservar a sua privacidade e proteger os seus dados pessoais, nenhuma transmissão de informações é totalmente segura, de modo que a INOVA não pode garantir integralmente que todas as informações que recebe e/ou envia não sejam alvo de acessos não autorizados perpetrados por meio de métodos desenvolvidos para obter informações de forma indevida, como falhas técnicas, vírus ou invasões do banco de dados do site.
De qualquer forma, na remota hipótese de incidência de episódios desta natureza, a INOVA garante o pleno esforço para remediar as consequências do evento.




6. Por quanto tempo a INOVA armazenará os seus dados?
 
Os seus dados pessoais serão automaticamente eliminados pela INOVA, quando deixarem de ser úteis para os fins que foram coletados e não forem mais necessários para cumprir qualquer obrigação legal direcionada à INOVA, ou quando o Usuário solicitar a sua eliminação, exceto se a manutenção do dado for expressamente autorizada por lei.
As informações dos Usuários poderão ser conservadas para cumprimento de obrigação legal ou regulatória, transferência a terceiro – desde que respeitados os requisitos de tratamento de dados – e uso exclusivo da INOVA, inclusive para o exercício de seus direitos em processos judiciais ou administrativos.
 
7. Quais são direitos como titular de dados pessoais?
A INOVA respeita e garante ao Usuário, a possibilidade de apresentar solicitações de tratamento dos seus dados pessoais, baseadas nos seguintes direitos:
A. Confirmação da existência de tratamento;
B. Acesso aos dados pessoais;
C. Correção de dados incompletos, inexatos ou desatualizados;
D. Anonimização , bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com a lei;
E. Portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa pelo Usuário;
F. Eliminação dos dados tratados com consentimento do Usuário;
G. Obtenção de informações sobre as entidades com as quais compartilhou os seus dados;
H. Informação sobre a possibilidade do Usuário não fornecer o consentimento, bem como de ser informado sobre as consequências em caso de negativa;
I. Revogação do consentimento.
 
8. Como exercer os seus direitos de titular dos dados?
 
Os seus direitos como titular de dados pessoais poderão ser exercidos diretamente por você, a partir da gestão de informações de cadastro, enquanto outra parte dependerá do envio de requisição por meio de solicitação em nosso SAC (21-31891100), para posterior avaliação e adoção de das providências necessárias.
O Usuário fica ciente que a eventual solicitação de exclusão de informações essenciais para a gestão de seu cadastro junto à INOVA implicará no término de sua relação contratual, com o consequente cancelamento dos serviços então prestados.
A INOVA empreenderá todos os esforços razoáveis para atender as requisições feitas por Usuários no menor espaço de tempo possível. No entanto, fatores justificáveis, tais como a complexidade da ação requisitada, poderão atrasar ou impedir o seu rápido atendimento, sendo certo que, em caso de demora, apresentará os devidos motivos.
Por fim, o Usuário deve estar ciente que a sua requisição poderá ser legalmente rejeitada, seja por motivos formais (a exemplo de sua incapacidade de comprovar sua identidade) ou legais (a exemplo do pedido de exclusão de dados cuja manutenção é livre exercício de direito pela INOVA), sendo certo que, na hipótese de impossibilidade de atendimento destas requisições, a INOVA apresentará as justificativas razoáveis.
 
9. O que são e como a INOVA utiliza a tecnologia cookies?
Cookies são arquivos ou informações que podem ser armazenadas em seus dispositivos quando você visita os websites ou utiliza os serviços on-line da INOVA. Geralmente, um cookie contém o nome do site que o originou, seu tempo de vida e um valor, que é gerado aleatoriamente.
A INOVA utiliza cookies para facilitar o uso e melhor adaptar seu website e aplicações aos interesses e necessidades de seus Usuários, bem como para auxiliar e melhorar suas estruturas e seus conteúdos. Os cookies também podem ser utilizados para acelerar suas atividades e experiências futuras em nossos serviços.
Por meio de cookies, o site armazena informações sobre as atividades do navegador, incluindo endereço IP e a página acessada. Esses registros de atividades (logs) serão utilizados apenas para fins estatísticos e de métricas dos serviços disponibilizados ou para a investigação de fraudes ou de alterações indevidas em seus sistemas e cadastros, não tendo como finalidade o fornecimento dos dados a terceiros sem autorização expressa do Usuário.
Esses registros poderão compreender dados como o endereço IP do Usuário, as ações efetuadas no site, as páginas acessadas, as datas e horários de cada ação e de acesso a cada página do site, as informações sobre o dispositivo utilizado, versão de sistema operacional, navegador, dentre outros aplicativos instalados.
Os cookies podem ser divididos de acordo com a informação que armazenam e sua finalidade, a saber:
A. Cookies necessários: São essenciais para que o website da INOVA carregue corretamente e permita que você navegue corretamente, bem como faça o uso de todas as funcionalidades disponíveis.
B. Cookies de Desempenho: Ajudam a entender como os visitantes interagem com a página da INOVA, fornecendo informações sobre as áreas visitadas, o tempo de visita ao site e quaisquer problemas encontrados, como mensagens de erro.
C. Cookies Funcionais: Permitem que a página se lembre de suas escolhas, para proporcionar uma experiência personalizada. Possibilitam que os Usuários assistam a vídeos, utilizem ferramentas sociais, campos para comentários, fóruns e dentre outros.
D. Cookies de Marketing: São utilizados para fornecer mais conteúdo relevante e do interesse dos Usuários. Podem ser utilizados para apresentar publicidade com um maior direcionamento ou limitar o número que esta é veiculada, nas páginas da INOVA. Também, permitem a medição da eficácia de uma campanha publicitária lançada.
A qualquer momento você poderá revogar a sua autorização quanto à utilização dos cookies, utilizando, as configurações de seu navegador de preferência.
